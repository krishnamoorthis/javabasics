package com.coditionsprogram;

public class BreakContinue {
	
		public static void main(String[] args) {
			int i=0;
			
		   for(i=0;i<10;i++){
			   if(i==3){
				continue;//  which means remove the particular value
			}
				System.out.println("for loop"+i);
			if(i==5){
				break;// stop the execution in require value
			}
		}
		
	}
	}


